#include <stdio.h>
#include <stdlib.h>

/**
    Qu’affiche ce programme après exécution
    Essayez de tourner à la main le programme
    avant de le compiler et de l'exécuter sur votre machine
*/
int main()
{
    int a = 10;
    int b = 10;
    printf("a++ = %d\n", a++);
    printf("a-- = %d\n", a--);
    printf("++b = %d\n", ++b);
    printf("--b = %d\n", --b);
    printf("a = %d\n", a);
    printf("b = %d\n", b);
    return 0;
}
